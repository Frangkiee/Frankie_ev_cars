-- 创建数据表
CREATE TABLE IF NOT EXISTS monthly_cars (
  id SERIAL PRIMARY KEY,
  year INTEGER NOT NULL,
  month INTEGER NOT NULL,
  group_name VARCHAR NOT NULL,
  name VARCHAR NOT NULL,
  oem VARCHAR NOT NULL,
  type VARCHAR NOT NULL,
  aer VARCHAR NOT NULL DEFAULT '-',
  weight VARCHAR NOT NULL DEFAULT '-',
  consumption VARCHAR NOT NULL DEFAULT '-',
  battery_type VARCHAR NOT NULL DEFAULT '-',
  battery_capacity VARCHAR NOT NULL DEFAULT '-',
  drive_mode VARCHAR NOT NULL DEFAULT '-',
  price VARCHAR NOT NULL DEFAULT '-',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_monthly_cars_year_month ON monthly_cars(year, month);
CREATE INDEX IF NOT EXISTS idx_monthly_cars_name ON monthly_cars(name);
CREATE INDEX IF NOT EXISTS idx_monthly_cars_group ON monthly_cars(group_name);

-- 启用 RLS (Row Level Security)
ALTER TABLE monthly_cars ENABLE ROW LEVEL SECURITY;

-- 创建公开读取策略（任何人可以读取数据）
CREATE POLICY "Allow public read access" ON monthly_cars
  FOR SELECT
  USING (true);

